    private void supprimer(){
        try{
            
            if(JOptionPane.showConfirmDialog(null, "Attention, voulez-vous vraiment faire cette opération de suppression?", "Suppression", JOptionPane.YES_NO_OPTION) == JOptionPane.OK_OPTION)
            if(txtid.getText().length()!=0){
                stm.executeUpdate("Delete From dettes where id = "+txtid.getText());
                afficher();
                JOptionPane.showMessageDialog(null, "La dette a été bien payée");
            }
            afficher();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Erreur de suppression"+e.getMessage());
        }
    }
    
    private void modifier(){
    try{
            if(JOptionPane.showConfirmDialog(null, "Confirmer la modification","Modification",
                JOptionPane.YES_NO_OPTION) == JOptionPane.OK_OPTION){
                SimpleDateFormat dt = new SimpleDateFormat();
                String ladate = dt.format(txtdate.getDate());
            stm.executeUpdate(
                    "UPDATE dettes SET nom='"
                    +txtnom.getText()+
                    "', montant='"
                    +txtmontant.getText()+
                    "',dateP='"+ladate+
                    "' WHERE id="+txtid.getText()
                );
            afficher();
        }
            
        }
        catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(null, "Erreur de modification"+e.getMessage());
            System.err.println(e);
            afficher();
        }
}
private void afficher(){
 try{
        model.setRowCount(0);
        stm = conn.obtenirconnexion().createStatement();
        ResultSet Rs = stm.executeQuery("Select * from dettes");
        while(Rs.next())
        {
            model.addRow(new Object[]{
                Rs.getString("id"),
                Rs.getString("nom"),
                Rs.getString("montant"),
                Rs.getString("dateP")}
            );
        }
        }
        catch(Exception e){
            System.err.println(e);
        }
        table.setModel(model);
}
private void deplace(int i){
   try{
     txtid.setText(model.getValueAt(i, 0).toString());
     txtnom.setText(model.getValueAt(i, 1).toString());
     txtmontant.setText(model.getValueAt(i, 2).toString());
     
   }
   catch(Exception e)
   {
       System.err.println(e);
       JOptionPane.showMessageDialog(null, "Erreur de deplacement"+e.getLocalizedMessage());
   }
}
