    private void ajouter(){
        String nom = txtnom.getText();
        SimpleDateFormat dt = new SimpleDateFormat();
        String ladate = dt.format(txtdate.getDate());
        String montant = txtmontant.getText();
        String requete = "insert into dettes(nom,montant,dateP) VALUES ('"+
        nom+"','"+
        montant+"','"+
        ladate+"')";
        try{
            stm.executeUpdate(requete);
            JOptionPane.showMessageDialog(null, "La dette a été bien ajouté");
        }
        catch(SQLException | HeadlessException ex){JOptionPane.showMessageDialog(null, ex.getLocalizedMessage());}
}
    private void search(){
     try{
        String [] col = {"Id","Nom", "Adresse", "Contact"};
        String [] aff = new String[4];
        DefaultTableModel mod= new DefaultTableModel(null, col);
        stm = conn.obtenirconnexion().createStatement();
        String search = txtsearch.getText();
        ResultSet Rs = null;
        Rs = stm.executeQuery("Select * from dettes where id like '%"+search+"%' or nom like '%"+search+"%' or montant like '%"+search+"%' or dateP like '%"+search+"%' ");
            while(Rs.next()){
              aff[0] = Rs.getString("id");
              aff[1] = Rs.getString("nom");
              aff[2] = Rs.getString("montant");
              aff[3] = Rs.getString("dateP");
              mod.addRow(aff);
            }
            table.setModel(mod);
     } 
     catch (SQLException ex) {
            Logger.getLogger(panelDettes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
